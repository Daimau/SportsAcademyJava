    private void regisbtnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regisbtnoActionPerformed
        try{
            File file =new File("Studentregistration.txt");
            String username=usertxt.getText().trim();
            String password=passtxt.getText().trim();
            String fname=fnametxt.getText().trim();
            String lname=lnametxt.getText().trim();
            String emal=emailtxt.getText().trim();
            String agie=agetxt.getText().trim();
            String number=notxt.getText().trim();
            String sprt=sportxt.getSelectedItem().toString();
            String spcenter=scentertxt.getSelectedItem().toString();
            String spbranch=branchtxt.getSelectedItem().toString();
            String coach=coachtxt.getText().trim();
            Student s= new Student(username,password,fname,lname,emal,agie,number,sprt,spcenter,spbranch,coach); //add coach variable after spbranch with a , then go to student class
                    
            try(PrintWriter writer=new PrintWriter(new BufferedWriter(new FileWriter("Studentregistration.txt",true))))
            {

                writer.println(s.getUsername()+","+s.getPassword()+","+s.getFname()+","+s.getLname()+","+s.getEmal()+","+s.getAgie()+","+s.getNumber()+","+s.getSprt()+","+s.getSpcenter()+","+s.getSpbranch()+","+s.getCoach()); //add coach here
                JOptionPane.showMessageDialog(null,"Thank you for registering, "+fname+"!");
                writer.close();
            }

        }

        catch(Exception e){
            JOptionPane.showMessageDialog(null,"Registration unsucessful");
        }
        StudentLogin lgp=new StudentLogin();
        lgp.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_regisbtnoActionPerformed

