    private void scentertxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_scentertxtActionPerformed
    try{
            if(scentertxt.getSelectedItem().equals("Kuala Lumpur")){
                branchtxt.removeAllItems();
                branchtxt.setSelectedItem(null);
                branchtxt.addItem("KL1");
                branchtxt.addItem("KL2");
                branchtxt.addItem("KL3");
                branchtxt.addItem("KL4");
            }
            else if(scentertxt.getSelectedItem().equals("Penang")){
                branchtxt.removeAllItems();
                branchtxt.setSelectedItem(null);
                branchtxt.addItem("P1");
                branchtxt.addItem("P2");
                branchtxt.addItem("P3");
                branchtxt.addItem("P4");
            }

            else if(scentertxt.getSelectedItem().equals("Melaka")){
                branchtxt.removeAllItems();
                branchtxt.setSelectedItem(null);
                branchtxt.addItem("M1");
                branchtxt.addItem("M2");
                branchtxt.addItem("M3");
                branchtxt.addItem("M4");
            }

            else if(scentertxt.getSelectedItem().equals("Lankawi")){
                branchtxt.removeAllItems();
                branchtxt.setSelectedItem(null);
                branchtxt.addItem("L1");
                branchtxt.addItem("L2");
                branchtxt.addItem("L3");
                branchtxt.addItem("L4");
            }
        }
        catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_scentertxtActionPerformed

